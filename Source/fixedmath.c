unsigned _r = 1;
unsigned rand()
{
  _r ^= _r << 7;
  _r ^= _r >> 9;
  _r ^= _r << 8;
  return _r;
}

void cross(vector3_t *out, const vector3_t *a, const vector3_t *b) 
{
  out->x = (a->y * b->z) - (b->y * a->z);
  out->y = (a->z * b->x) - (b->z * a->x);
  out->z = (a->x * b->y) - (b->x * a->y);
}

int dot(const vector3_t *a, const vector3_t *b) 
{
  return (a->x * b->x) + (a->y * b->y) + (a->z * b->z);
}

int square_magnitude(const vector3_t *v)
{
  return ((v->x * v->x) + (v->y * v->y) + (v->z * v->z));
}

int magnitude(const vector3_t *v)
{
  return sqrt(square_magnitude(v));
}

void normal(vector3_t *out, const vector3_t *a, const vector3_t *b, const vector3_t *c)
{
  vector3_t v1, v2;

  v1.x = b->x - a->x; v1.y = b->y - a->y; v1.z = b->z - a->z;
  v2.x = c->x - a->x; v2.y = c->y - a->y; v2.z = c->z - a->z;

  cross(out, &v2, &v1);
}

void rotate(matrix3x3_t m, int x, int y, int z)
{
    int cx = (SinLUT[(x + 64) & 255]);
    int sx = (SinLUT[(x)&255]);
    int cy = (SinLUT[(y + 64) & 255]);;
    int sy = (SinLUT[(y)&255]);
    int cz = (SinLUT[(z + 64) & 255]);
    int sz = (SinLUT[(z)&255]);

    m[0][0] = ((cx * cz) >> 8) - ((sx * sy * sz) >> 16);
    m[0][1] = ((sx * cz) >> 8) + ((cx * sy * sz) >> 16);
    m[0][2] = ((cy * sz) >> 8);

    m[1][0] = -(sx * cy) >> 8;
    m[1][1] =  (cx * cy) >> 8;
    m[1][2] = -sy;

    m[2][0] = -((cx * sz) >> 8) - ((sx * sy * cz) >> 16);
    m[2][1] =  ((cx * sy* cz) >> 16) - ((sx * sz) >> 8);
    m[2][2] =  ((cy * cz) >> 8);
}

